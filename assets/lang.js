// ===== Language Switcher Logic =====
let currentLang = localStorage.getItem('lang') || 'en';
const flags = { en: 'flags/gb.png', ru: 'flags/ru.png', kz: 'flags/kz.png' };

function setLang(l) {
  currentLang = l;
  localStorage.setItem('lang', l);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (LANGS[l][key]) el.textContent = LANGS[l][key];
  });
  document.getElementById('langFlag').src = 'assets/' + flags[l];
  document.getElementById('langName').textContent = l.toUpperCase();
}

document.addEventListener('DOMContentLoaded', () => {
  const btn = document.querySelector('.lang-selected');
  const drop = document.querySelector('.lang-dropdown');
  btn.addEventListener('click', () => drop.classList.toggle('open'));
  drop.querySelectorAll('.lang-item').forEach(i => i.addEventListener('click', () => {
    setLang(i.dataset.lang);
    drop.classList.remove('open');
  }));
  setLang(currentLang);
});

window.addEventListener('scroll', () => {
  const h = document.querySelector('header');
  if (window.scrollY > 80) h.classList.add('scrolled');
  else h.classList.remove('scrolled');
});
